#include "libft.h"

char	*ft_strdup(const char *src)
{
	char		*p;
	int			i;

	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	p = malloc(sizeof(char) * (i + 1));
	if (!p)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
